_DESKTOP_ENTRY = """\
[Desktop Entry]
Type=Application
Name={app_name}
Exec=main
Icon={icon_name}
Categories=Utility;Security;
Comment=Facial Recognition Security System v{app_version}
Terminal=false
"""

_APPRUN = """\
#!/bin/bash
# AppRun — entry point for the AppImage
SELF=$(readlink -f "$0")
HERE=$(dirname "$SELF")

# Ensure runtime dirs exist with correct permissions
mkdir -p "$HERE/faces_db/good" "$HERE/faces_db/bad"
mkdir -p "$HERE/metadata" "$HERE/targets" "$HERE/cache"
mkdir -p "$HERE/splash_assets" "$HERE/models"

export LD_LIBRARY_PATH="$HERE/usr/lib:$HERE:$LD_LIBRARY_PATH"
export PATH="$HERE/usr/bin:$HERE:$PATH"

exec "$HERE/main" "$@"
"""


def _find_appimagetool() -> str:
    """Locate appimagetool binary."""
    env = os.environ.get("APPIMAGETOOL", "")
    if env and os.path.isfile(env):
        return env
    found = shutil.which("appimagetool") or shutil.which("appimagetool-x86_64.AppImage")
    return found or ""


def _convert_ico_to_png(ico_path: str, png_path: str):
    """Best-effort conversion of .ico → .png using Pillow or magick."""
    try:
        from PIL import Image
        img = Image.open(ico_path)
        # Pick the largest icon size available
        sizes = img.info.get("sizes", set())
        if sizes:
            largest = max(sizes, key=lambda s: s[0] * s[1])
            img.size = largest
        img.save(png_path, format="PNG")
        return True
    except ImportError:
        pass

    magick = shutil.which("magick") or shutil.which("convert")
    if magick:
        subprocess.run([magick, ico_path, "-strip", png_path],
                       capture_output=True)
        if os.path.isfile(png_path):
            return True

    return False


def build_linux_installer():
    """Package the Nuitka dist into an AppImage."""
    print("\n[installer] Building Linux AppImage …")

    if not os.path.isdir(DIST_DIR):
        sys.exit(f"[ERROR] Nuitka dist not found at: {DIST_DIR}\n"
                 "        Run build.py first (on Linux or cross-compile).")

    appimagetool = _find_appimagetool()
    if not appimagetool:
        sys.exit(
            "[ERROR] appimagetool not found.\n"
            "        Download from: https://appimage.github.io/appimagetool/\n"
            "        Place it on PATH or set APPIMAGETOOL=/path/to/appimagetool"
        )

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    # Build an AppDir structure
    appdir = os.path.join(OUTPUT_DIR, f"{SAFE_NAME}.AppDir")
    if os.path.exists(appdir):
        shutil.rmtree(appdir)

    # Copy entire Nuitka dist into AppDir root
    print(f"[installer] Copying dist to AppDir …")
    shutil.copytree(DIST_DIR, appdir)

    # Ensure runtime directories exist inside the AppDir
    for subdir in [
        "faces_db/good", "faces_db/bad",
        "metadata", "targets", "cache",
        "splash_assets", "models",
    ]:
        os.makedirs(os.path.join(appdir, subdir), exist_ok=True)

    # Make the main binary executable
    main_bin = os.path.join(appdir, "main")
    if os.path.isfile(main_bin):
        os.chmod(main_bin, os.stat(main_bin).st_mode | stat.S_IXUSR | stat.S_IXGRP | stat.S_IXOTH)

    # Write AppRun
    apprun_path = os.path.join(appdir, "AppRun")
    with open(apprun_path, "w", newline="\n") as f:
        f.write(_APPRUN)
    os.chmod(apprun_path, 0o755)

    # Write .desktop file
    icon_name = SAFE_NAME.lower()
    desktop_path = os.path.join(appdir, f"{SAFE_NAME}.desktop")
    with open(desktop_path, "w", newline="\n") as f:
        f.write(_DESKTOP_ENTRY.format(
            app_name=APP_NAME,
            app_version=APP_VERSION,
            icon_name=icon_name,
        ))

    # Icon — AppImage expects a PNG at the AppDir root
    icon_png = os.path.join(appdir, f"{icon_name}.png")
    if os.path.isfile(ICON_FILE):
        if ICON_FILE.lower().endswith(".png"):
            shutil.copy2(ICON_FILE, icon_png)
        else:
            if not _convert_ico_to_png(ICON_FILE, icon_png):
                # Fallback: copy the .ico as-is (appimagetool may still accept it)
                shutil.copy2(ICON_FILE, icon_png)
                print("[installer] Warning: could not convert .ico to .png — "
                      "install Pillow or ImageMagick for best results.")
    else:
        print(f"[installer] Warning: icon not found at {ICON_FILE}")

    # Build the AppImage
    output_name = f"{SAFE_NAME}-{APP_VERSION}-x86_64.AppImage"
    output_path = os.path.join(OUTPUT_DIR, output_name)

    print(f"[installer] Running appimagetool …")
    env = os.environ.copy()
    env["ARCH"] = "x86_64"
    result = subprocess.run(
        [appimagetool, appdir, output_path],
        cwd=SCRIPT_DIR,
        env=env,
    )
    if result.returncode != 0:
        sys.exit("[ERROR] appimagetool failed.")

    # Cleanup AppDir (the .AppImage is self-contained)
    shutil.rmtree(appdir, ignore_errors=True)

    print(f"[installer] AppImage ready: {output_path}")
    return output_path


# ═══════════════════════════════════════════════════════════════════════════
# CLI
# ═══════════════════════════════════════════════════════════════════════════

def main():
    parser = argparse.ArgumentParser(
        description="Build platform installers from a Nuitka dist."
    )
    parser.add_argument(
        "--platform",
        choices=["windows", "linux", "all"],
        default=None,
        help="Target platform (default: auto-detect current OS)",
    )
    args = parser.parse_args()

    target = args.platform
    if target is None:
        target = "windows" if platform.system() == "Windows" else "linux"

    print(f"[installer] App: {APP_NAME}  v{APP_VERSION}")
    print(f"[installer] Dist: {DIST_DIR}")
    print(f"[installer] Output: {OUTPUT_DIR}")

    results = []

    if target in ("windows", "all"):
        results.append(build_windows_installer())

    if target in ("linux", "all"):
        results.append(build_linux_installer())

    print("\n" + "=" * 60)
    print("[installer] Done! Generated installers:")
    for r in results:
        print(f"  • {r}")
    print("=" * 60)


if __name__ == "__main__":
    main()
